package com.example.myapp;

public class NewFuction {
    public int getSum(int a, int b){
        return a+b;
    }

    public int getSub(int a, int b){
        return a-b;
    }

    public int getSub(int a, int b, int c){
        return a+b+c;
    }

    public int getSub(int a, int b, int c, int d, int f){
        return a+b+c+d+f;
    }
}
